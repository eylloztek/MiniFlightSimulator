void ADXL_Write(uint8_t reg, uint8_t value) {
	uint8_t data[2];
	data[0] = reg;
	data[1] = value;
	HAL_I2C_Master_Transmit(&hi2c1, ADXL_ADDR, data, 2, 100);
}

void ADXL_Read(uint8_t reg, uint8_t *buffer, uint8_t size) {
	HAL_I2C_Master_Transmit(&hi2c1, ADXL_ADDR, &reg, 1, 100);
	HAL_I2C_Master_Receive(&hi2c1, ADXL_ADDR, buffer, size, 100);
}

void ADXL_Init(void) {
	ADXL_Write(0x2D, 0x08);  // Measurement mode
	ADXL_Write(0x31, 0x00);  // Â±2g
}

